<STRONG>§1. Namn</STRONG><BR>
Föreningens namn är Altkonst.<P>

<STRONG>§2. Form</STRONG><BR>
Altkonst är en ideell förening. Altkonst står självständig gentemot stater, kommuner, religiösa, politiska och privata intressen. Föreningens säte är i Stockholm.<P>

<STRONG>§3. SyftAltkonste och målsättning</STRONG><BR>
Altkonst är en öppen förening.<BR>
Föreningen skall stödja och uppmuntra medlemmarnas kulturella och sociala engagemang. Föreningen skall också verka för att medlemmarna, genom kunskaps- och erfarenhetsutbyte, har möjlighet att utvecklas kreativt och socialt.<BR>
En stor del av Altkonsts arbete fokuserar på gatuteater, konst, hantverk, nycirkus, teater och musik i alla dess former.<BR>
Altkonst skall vara öppen för nya konstformer och sätt för individer att uttrycka sig, samt främja ett samhälle där detta är möjligt.<BR>
Altkonst arbetar aktivt för att motverka främlingsfientlighet, diskriminering samt mobbing av alla dess slag.<BR>
Altkonst arbetar för att främja direktdemokrati.<P>

<STRONG>§4. Medlemskap</STRONG><BR>
Föreningen är öppen för alla fysiska personer. Medlem är den som godkänt medlemskap i en av Altkonsts lokalavdelningar. Medlem har rösträtt på medlems- och årsmöte. Medlem ansvarar för att verka i enlighet med Altkonsts målsättning och uppfyller de av Altkonst stadgade kraven.<BR>
Medlem uppmuntras att engagera sig i möten och att utveckla föreningen. Medlem ansvarar också för att följa alla policydokument.<P>

<STRONG>§5. Lokalavdelning</STRONG><BR>
Beslut om startande av lokalavdelning, sammanslagning av lokalavdelning eller delning av lokalavdelning görs av styrelsen. Alla lokalavdelningar är upptecknade i Altkonsts lista över lokalavdelningar. Alla lokalavdelningar använder Altkonsts mall för lokalavdelningar. Förändringar i mallen för lokalavdelningar tas upp på årsmöte i form av motioner.<P>

<STRONG>§6. Kommunikation</STRONG><BR>
Intern kommunikation i föreningen sker genom elektronisk media. Föreningens plattform är http://www.eldsjal.org.<BR>
Alla kallelser, policydokument, protokoll samt motioner skall finnas tillgängliga för medlemmar via plattformen eller e-post, medlem ansvarar för att ta del av dessa. Motioner skall publiceras på plattformen minst 24 timmar innan medlems- eller årsmöte.<P>

<STRONG>§7. Möten</STRONG><BR>
Medlemsmöte behandlar propositioner, motioner och förslag. Medlemsmöte skall utlysas minst 72 timmar i förväg.<BR>
Alla medlemmar kan utlysa ett medlemsmöte.<BR>
Årsmöte skall utlysas minst 14 dagar i förväg av styrelsen och skall behandla följande:<BR>
- verksamhetsberättelse<BR>
- bokslut<BR>
- ansvarsfrihet för avgående styrelse<BR>
- val av ny styrelse<BR>
- val av revisor<BR>
- fastställande av förmånsmedlemsavgift<BR>
- propositioner, motioner och förslag<BR>
- val av valberedning som skall bestå av 3 fysiska personer.<BR>
Vid alla möten skall en majoritet av styrelsen närvara för att mötet skall vara giltigt. Årsmöte skall hållas senast juni månad.<P>

<STRONG>§8. Styrelse</STRONG><BR>
Styrelsen beslutar över löpande frågor och är underställd medlems- och årsmöte. En majoritet av styrelsen måste närvara för att styrelsen skall vara
beslutsmässig. Styrelsen skall sträva efter konsensusbeslut, vid oenighet gäller enkel majoritet. Styrelsemöte skall hållas minst tre gånger per år.<BR>
Styrelsen skall bestå av 7 - 11 ordinarie ledamöter. Styrelsen konstituerar sig själva med undantag av ordförande och kassör som väljs vid årsmöte.<BR>
Nya ersättare för styrelsemedlemmar väljs vid medlemsmöte.<P>

<STRONG>§9. Firmatecknare</STRONG><BR>
Firmatecknare för föreningen Altkonst är nuvarande Ordförande och Kassör.<P>

<STRONG>§10. Verksamhetsår</STRONG><BR>
Föreningens verksamhetsår löper från första januari till sista december.<P>

<STRONG>§11. Uteslutning</STRONG><BR>
Medlem som klart bryter mot Altkonsts stadgar, kan av styrelsen uteslutas. Den uteslutne har rätt att överklaga beslutet vid medlemsmöte där den uteslutne har rösträtt. Styrelsen förbehåller sig rätten att direkt utesluta medlem som, i Altkonsts namn, begår lagbrott.<P>

<STRONG>§12. Omröstning</STRONG><BR>
Vid års- och medlemsmöte gäller följande:<BR>
- Vid beslut skall konsensus eftersträvas.<BR>
- Vid lika röstetal avgör ordförandes röst. Vid lika röstetal i personval sker omröstning mellan de två starkaste kandidaterna, om majoritet ej erhålles sker lottning.<P>

<STRONG>§13. Stadgeändring</STRONG><BR>
För att ändra Altkonsts stadgar krävs 2/3 majoritet vid ett årsmöte. Beslutet träder i kraft 30 dagar efter att beslutet delgivits föreningsmedlemmarna, förutsatt att ingen protest har lämnats in.<P>

En föreningsmedlem har rätt att lämmna in en protest mot stadgeändring.<P>

En protest mot stadgeändring ska vara styrelsen tillhanda skriftligen inom 30 dagar efter det att beslut rörande stadgeändring delgivits föreningsmedlemmarna. Då protest lämnas in krävs ytterligare ett medlemsmöte med 2/3 majoritet för att ändra dessa stadgar. Beslutet träder i kraft därefter.<P>

